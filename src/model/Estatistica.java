package model;

public class Estatistica {
	public int Cod_Cidade;
	public String Nome_Cidade;
	public int QTD_Acidentes;
	public int Tipo_Veiculo;
	public int getCod_Cidade() {
		return Cod_Cidade;
	}
	public void setCod_Cidade(int cod_Cidade) {
		Cod_Cidade = cod_Cidade;
	}
	public String getNome_Cidade() {
		return Nome_Cidade;
	}
	public void setNome_Cidade(String nome_Cidade) {
		Nome_Cidade = nome_Cidade;
	}
	public int getQTD_Acidentes() {
		return QTD_Acidentes;
	}
	public void setQTD_Acidentes(int qTD_Acidentes) {
		QTD_Acidentes = qTD_Acidentes;
	}
	public int getTipo_Veiculo() {
		return Tipo_Veiculo;
	}
	public void setTipo_Veiculo(int tipo_Veiculo) {
		Tipo_Veiculo = tipo_Veiculo;
	}
	
}
